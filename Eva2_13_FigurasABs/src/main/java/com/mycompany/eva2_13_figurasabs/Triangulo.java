public class Triangulo {
    private double base; 
    private double Altrua; 

    public Triangulo(double base, double Altrua) {
        this.base = base;
        this.Altrua = Altrua;
    }

    public double getBase() {
        return base;
    }

    public double getAltrua() {
        return Altrua;
    }

    public void setBase(double base) {
        this.base = base;
    }

    public void setAltrua(double Altrua) {
        this.Altrua = Altrua;
    }
        
}







